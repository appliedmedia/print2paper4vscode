#!/usr/bin/env node
/* global console, process */
// eslint-disable

/**
 * convertVSCodeTsForExport.js
 *
 * This script converts the official VS Code TypeScript definitions
 * from @types/vscode/index.d.ts into a format suitable for export
 * and use in our extension.
 *
 * What it does:
 * 1. Reads the official VS Code type definitions from node_modules/@types/vscode/index.d.ts
 * 2. Transforms "declare module 'vscode'" to "export namespace vscode"
 * 3. Adds a header with generation timestamp and information
 * 4. Writes the result to src/lib/vscodeapis_stubs.ts
 *
 * Usage: node build/convertVSCodeTsForExport.js
 */

import fs from 'fs';
import path from 'path';

console.log('🔧 VS Code TypeScript Definitions Converter');
console.log('==========================================\n');

function convertVSCodeTypes() {
  try {
    // Get the project root directory (where this script is run from)
    const projectRoot = process.cwd();

    // Path to the official VS Code type definitions
    const typeDefPath = path.join(projectRoot, 'node_modules', '@types', 'vscode', 'index.d.ts');

    console.log('📁 Reading VS Code type definitions from:');
    console.log(`   ${typeDefPath}\n`);

    if (!fs.existsSync(typeDefPath)) {
      throw new Error(`VS Code type definitions not found at: ${typeDefPath}`);
    }

    // Read the type definitions file
    const typeDefContent = fs.readFileSync(typeDefPath, 'utf8');
    console.log(`✅ Read ${typeDefContent.length} characters from type definitions\n`);

    // Extract the content between the module declaration
    const moduleMatch = typeDefContent.match(/declare module 'vscode' \{([\s\S]*)\}/);
    if (!moduleMatch) {
      throw new Error('Could not parse VS Code type definitions - module declaration not found');
    }

    const moduleContent = moduleMatch[1];
    console.log(`✅ Extracted ${moduleContent.length} characters of module content\n`);

    // Create the lib directory if it doesn't exist
    const libDir = path.join(projectRoot, 'src', 'lib');
    if (!fs.existsSync(libDir)) {
      fs.mkdirSync(libDir, { recursive: true });
      console.log('📁 Created src/lib directory\n');
    }

    // Create our stub file with proper header
    const timestamp = new Date().toISOString();
    let result = '// Auto-generated VS Code API stubs from official type definitions\n';
    result += '// Generated by: build/convertVSCodeTsForExport.js\n';
    result += `// Generated on: ${timestamp}\n`;
    result += `// Source: ${typeDefPath}\n\n`;
    result += '// eslint-disable-next-line @typescript-eslint/prefer-namespace-keyword\n';
    result += 'export namespace vscode {\n';
    result += moduleContent;
    result += '}\n';

    // Write to the lib directory
    const outputPath = path.join(libDir, 'vscodeapis_stubs.ts');
    fs.writeFileSync(outputPath, result, 'utf8');

    console.log('✅ Successfully converted VS Code type definitions!');
    console.log(`📄 Output written to: ${outputPath}`);
    console.log(`📊 Final file size: ${result.length} characters\n`);

    // Show some stats about what was generated
    const lines = result.split('\n').length;
    const interfaces = (result.match(/export interface/g) || []).length;
    const functions = (result.match(/export function/g) || []).length;
    const constants = (result.match(/export const/g) || []).length;
    const types = (result.match(/export type/g) || []).length;

    console.log('📈 Generation Statistics:');
    console.log(`   Total lines: ${lines}`);
    console.log(`   Interfaces: ${interfaces}`);
    console.log(`   Functions: ${functions}`);
    console.log(`   Constants: ${constants}`);
    console.log(`   Types: ${types}\n`);

    console.log('🎉 Conversion complete! The file is ready for use in your extension.');
  } catch (error) {
    console.error('❌ Error during conversion:', error.message);
    process.exit(1);
  }
}

// Run the conversion
convertVSCodeTypes();
